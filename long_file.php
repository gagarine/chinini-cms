                                                <div class="content">
                                                    <p>
                                                        Lorsque l'on clique sur un lien dans Firefox, un cadre pointill&#xE9; apparait autour. Celui-ci peut parfois &#xEA;tre visuellement g&#xEA;nant.
                                                    </p>
                                                    <p>
                                                        Pour &#xE9;viter ceci, une simple ligne dans votre CSS suffira:
                                                    </p>
                                                    <p>
                                                        <code>
                                                            <br />
                                                            a:HOVER { outline:0; }
                                                            <br />
                                                        </code>
                                                    </p>
                                                    <p>
                                                        Pourquoi <em>a:HOVER</em>
                                                        et pas simplement <em>a</em>? Pour ne pas perdre l'encadr&#xE9; lorsque l'on utilise la touche [Tab] pour parcourir les liens.
                                                    </p>
                                                    <p>
                                                        Voil&#xE0;
                                                    </p>
